#include "ibuserror.h"

IBusError *
ibus_error_new (void)
{
    return g_slice_new0 (IBusError);
}

void
ibus_error_free (IBusError *error)
{
    dbus_error_free (error);
    g_slice_free (IBusError, error);
}

